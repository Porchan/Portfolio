const PAGE_SIZE = 5;
let currentFilter = 'all';
let visibleCount = PAGE_SIZE;

function getLang() {
  return document.getElementById('btn-en').classList.contains('active') ? 'en' : 'pt';
}
function getFiltered() {
  return currentFilter === 'all' ? PROJECTS : PROJECTS.filter(p => p.tag === currentFilter);
}
function buildCard(p, animIndex) {
  const lang = getLang();
  const name = lang === 'en' ? p.nameEn : p.namePt;
  const tagLabel = (p.tag === 'Publicidade' && lang === 'en') ? 'Advertising' : p.tag;
  const div = document.createElement('div');
  div.className = 'p-item ' + p.span + ' entering';
  div.style.animationDelay = (animIndex * 80) + 'ms';
  div.innerHTML =
    '<div class="p-thumb">' +
      '<div class="p-thumb-bg ' + p.tb + '">' + p.emoji + '</div>' +
      '<div class="p-play"><div class="p-play-circle">&#9654;</div></div>' +
    '</div>' +
    '<div class="p-info">' +
      '<div class="p-tag">' + tagLabel + '</div>' +
      '<div class="p-name">' + name + '</div>' +
    '</div>';
  div.addEventListener('mouseenter', () => { cur.classList.add('hov'); ring.classList.add('hov'); });
  div.addEventListener('mouseleave', () => { cur.classList.remove('hov'); ring.classList.remove('hov'); });
  return div;
}
function renderPortfolio() {
  const grid = document.getElementById('portfolioGrid');
  const filtered = getFiltered();
  const currentLen = grid.children.length;
  filtered.slice(currentLen, visibleCount).forEach((p, i) => grid.appendChild(buildCard(p, i)));
  document.getElementById('loadMoreRow').style.display = visibleCount >= filtered.length ? 'none' : 'flex';
}
function resetPortfolio() {
  document.getElementById('portfolioGrid').innerHTML = '';
  visibleCount = PAGE_SIZE;
  renderPortfolio();
}

document.getElementById('loadMoreBtn').addEventListener('click', () => {
  visibleCount += PAGE_SIZE;
  renderPortfolio();
});
document.querySelectorAll('.filter-btn[data-filter]').forEach(btn => {
  btn.addEventListener('click', function() {
    document.querySelectorAll('.filter-btn[data-filter]').forEach(b => b.classList.remove('active'));
    this.classList.add('active');
    currentFilter = this.getAttribute('data-filter');
    resetPortfolio();
  });
});

